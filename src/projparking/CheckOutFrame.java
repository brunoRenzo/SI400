    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        FileManager fm = new FileManager();
        try {
            fm.getParkers();
            try {
                Parker p=fm.getParker(txtPlaca.getText());
                if(p!=null){
                    lblCor.setText(p.getCor());
                    lblModelo.setText(p.getModelo());
                    lblVaga.setText(p.getVaga());
                    
                    LocalDateTime dhCi = p.getDataHora();
                    lblCi.setText(dhCi.getHour()+":"+dhCi.getMinute()+"h");
                    
                    LocalDateTime dhCo = LocalDateTime.now();
                    lblCo.setText(dhCo.getHour()+":"+dhCo.getMinute()+"h");
                    
                    
                    
                    int parkingTime = ((dhCo.getHour()*60)+dhCo.getMinute())-((dhCi.getHour()*60)+(dhCi.getMinute()));
                    int completeHours = parkingTime/60;//arredonda para baixo
                    int minutes = parkingTime-(completeHours*60);
                    
                    Taxing t = new Taxing();
                    float parkerTax = 0;
                    float[] possiblePrices = t.load();
                    
                    if(completeHours<1){
                        parkerTax=possiblePrices[0];
                    }
                    else if(completeHours<4){
                        parkerTax=possiblePrices[1];
                    }
                    else if(completeHours<8){
                        parkerTax=possiblePrices[2];
                    }
                    else if(completeHours<24){
                        parkerTax=possiblePrices[3];
                    }
                    else{
                        parkerTax=possiblePrices[3]*(completeHours/24);
                    }
                    
                    System.out.println(completeHours+":"+minutes+"h"); 
                    lblPreco.setForeground(Color.red);
                    lblPreco.setFont(lblPreco.getFont().deriveFont(14));
                    lblPreco.setText("R$ "+parkerTax);
                    
                    btnConclude.setEnabled(true);
                    
                }
                else {
                    JOptionPane.showMessageDialog(this,"Essa placa n√£o corresponde a nenhum carro cadastrado","Campos inv√°lidos",JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
            Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (IOException ex) {
            Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnConcludeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcludeActionPerformed
        if(!txtPlaca.getText().equals("")){
            FileManager fm = new FileManager();
            try {
                fm.removeParker(txtPlaca.getText());
                JOptionPane.showMessageDialog(this,"Check-out efetuado com sucesso");
            } catch (IOException ex) {
                Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Ocorreu uma falha, tente novamente.","Falha no sistema",JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CheckOutFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this,"Ocorreu uma falha, tente novamente.","Falha no sistema",JOptionPane.ERROR_MESSAGE);
            }
            finally{
                IndexFrame indexF = new IndexFrame();
                indexF.setVisible(true);
                this.setVisible(false);
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"A placa do ve√≠culo precissa ser informada","Campos inv√°lidos",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConcludeActionPerformed

